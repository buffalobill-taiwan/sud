export function isWide(ch) {
    const code = typeof ch === 'string' ? ch.charCodeAt(0) : ch;
    let lo = 0;
    let hi = ranges.length - 1;
    while (lo <= hi) {
        const mid = (lo + hi) >> 1;
        const [l, r] = ranges[mid];
        if (code < l) hi = mid - 1;
        else if (code > r) lo = mid + 1;
        else return true;
    }
    return false;
}
